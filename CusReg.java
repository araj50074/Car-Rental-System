    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        String value=jTable1.getModel().getValueAt(row , 0).toString();
        String sql="Select * from CusReg where CusID='"+value+"'";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                String jtf1=rs.getString("CusID");
                jTextField1.setText(jtf1);
                String jtf2=rs.getString("CusName");
                jTextField2.setText(jtf2);
                String jtf3=rs.getString("Address");
                jTextArea1.setText(jtf3);
                String jtf4=rs.getString("Mobile");
                jTextField3.setText(jtf4);
                String jtf5=rs.getString("Email");
                jTextField4.setText(jtf5);
                jTextField1.setEditable(false);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

