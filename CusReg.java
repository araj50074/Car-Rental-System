    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql="Insert into CusReg (CusID ,CusName ,Address ,Mobile ,Email) values(?,?,?,?,?)";
        try{
            pst=conn.prepareStatement(sql);
            String jtf1=jTextField1.getText();
            String jtf2=jTextField2.getText();
            String jtf3=jTextArea1.getText();
            String jtf4=jTextField3.getText();
            String jtf5=jTextField4.getText();
             
            pst.setString(1, jtf1);
            pst.setString(2,jtf2);
            pst.setString(3,jtf3);
            pst.setString(4, jtf4);
            pst.setString(5, jtf5);
            if(jtf1.trim().equals("")||jtf2.trim().equals("")||jtf3.trim().equals("")||jtf4.trim().equals("")||jtf5.trim().equals(""))
            {
                JOptionPane.showMessageDialog(null,"One or More boxes are empty,Please Check");
            }else{
                pst.execute();
                JOptionPane.showMessageDialog(null, "Customer added successfully");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextArea1.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                table1();
                jTextField1.setEditable(true);
            }
        }catch(SQLIntegrityConstraintViolationException ex){
            JOptionPane.showMessageDialog(null,"Customer ID already taken,Choose another one");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

